let weather = 'rainy';

switch (weather) {
  case 'sunny':
    console.log('It\'s a beautiful day!');
    break;
  case 'rainy':
    console.log('Grab your umbrella.');
    break;
  case 'snowy':
    console.log('Do you wanna build a snowman?');
    break;
  default:
    console.log('Let\'s stay inside.');
}
